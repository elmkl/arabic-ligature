def query_letter(letter: str):
    ligature_dicts = [ARABIC_LETTERS, LIGATURE_TWO_ELEMENTS, 
                      LIGATURE_THREE_ELEMENTS, WORD_LIGATURES, 
                      CURRENCY_SYMBOLS, HONORIFIC_WORD_LIGATURES, FONT]
    for ligature_dict in ligature_dicts:
        if letter in ligature_dict:
            return ligature_dict[letter][0]
    return letter

def query_string(string: str):
    new_string = []
    for letter in string:
        new_string.append(query_letter(letter))
    return "".join(new_string)
